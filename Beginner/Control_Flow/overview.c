/*
Statements are exectuted sequentially (top to bottom).

Control flow statements will break this up by:
    - Decision-making statements (if-then, if-then-else, switch, goto)
    - Looping stements (for, while, do-while)
    - Branching statements (break, continue, return)

Decision Making:
    - specify a conditin to test.
    - if true, execute code
    - if false, execute other code or don't.

If statements:
    - if statement: consists of a boolean expression followed by one or more statements.
    - if...else statement: if can be followed by an optional else statement, which executes when the boolan expression is false
    - nested if statements: you can use one if or else if statement inside another if or else if statements

Loops:
    - While loop: It repeats a statement or a group of statements while a given conditin is true.
        It tests the condition before executing the loop body.
    - for loop: It executes a sequence of statements multiple times and abbreviates the code that manages the loop variable.
    - do...while loop it is similar to a while statement, except that it tests the condition at the end of the loop body.
    - Nested loops: You can use one or more loop inside any other while, for or do...while loops.
*/

